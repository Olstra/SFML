using namespace sf;

int main() {
    const int windowW = 1500;
    const int windowH = 1500;
    
    // Create the main window
    RenderWindow window(VideoMode(windowW, windowH), "OLIVER CODES AN ICON");
    window.setIcon( gimp_image.width,  gimp_image.height,  gimp_image.pixel_data );

	// Start the game loop
    while (window.isOpen()) {
        // Process events
        Event event;
        while (window.pollEvent(event)) {
            if (event.type == Event::Closed) {  window.close();  }

            if(event.type ==  Event::KeyPressed) {
                switch (event.key.code) {
                        case Keyboard::Enter:
                            break;

                    case Keyboard::X:
                        window.close();
                        break;

                    default: break;
                }
            }
        }


        // Clear screen
        window.clear();

        // Update the window
        window.display();
    }

    return 0;
}
